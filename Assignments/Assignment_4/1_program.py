"""
Program 1:
WAP to print numbers from a given range.
Input :
Start = 100;
End = 110;
Output:
100 101 103 104 105 106 107 108 109

"""


start = int(input("Enter value of start : "))
end = int(input("Enter value of end : "))


for i in range(start,end):
    print(i,end = " ")




#O/p 100 101 102 103 104 105 106 107 108 109

