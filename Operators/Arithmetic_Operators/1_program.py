#Arithmetic_Operators

x = 20
y = 10

print(x + y)      #30                 Addtion
print(x - y)      #10                 Substraction
print(x * y)      #200                Multiplication
print(x / y)      #2.0                Division(float)
print(x % y)      #0                  Modulas
print(x // y)     #2                  Division(floor)
print(x ** y)     #10240000000000     Power    20*20*20*20*20*20*20*20*20*20
